***********************************************************
* Music_RefillBuffer_Impl
*   Generate 256 samples into Sound_PageBuffer.
*
*   Optimized inner loop: uses addd for phase advance (saves 13 cycles
*   per voice vs ldy/leay/sty/tfr), and voice 0 stores raw wavetable
*   value (skips init + subb, saves 9 cycles per sample).
***********************************************************
*
Music_Sample    rmb     1
*
Music_RefillBuffer_Impl
            ldu         #Sound_PageBuffer
            lda         Music_Playing
            cmpa        #2
            beq         MusicRefillFade
            andcc       #$AF
MusicRefillLoop@
            * --- Voice 0 (stores raw value, no centering needed) ---
            ldd         Music_PhaseInc0         * 6
            addd        Music_PhaseAccum0       * 7
            std         Music_PhaseAccum0       * 6
            tfr         a,b                     * 6
            andb        #$7F                    * 2
            ldx         Music_WavePtr0          * 6
            abx                                 * 3
            ldb         ,x                      * 4
            tsta                                * 2
            bpl         V0Store@                * 3
            negb                                * 2
V0Store@
 IFEQ MUSIC_VOICES-1
            orb         #2                      * 1 voice: write directly to output
            stb         ,u+
 ELSE
            stb         Music_Sample            * multi-voice: save for accumulation
 ENDC
 IFGE MUSIC_VOICES-2
            * --- Voice 1 ---
            ldd         Music_PhaseInc1         * 6
            addd        Music_PhaseAccum1       * 7
            std         Music_PhaseAccum1       * 6
            tfr         a,b                     * 6
            andb        #$7F                    * 2
            ldx         Music_WavePtr1          * 6
            abx                                 * 3
            ldb         ,x                      * 4
            tsta                                * 2
            bpl         V1Store@                * 3
            negb                                * 2
V1Store@    subb        #$80                    * 2 (signed offset from center)
            addb        Music_Sample            * 5
  IFGE MUSIC_VOICES-3
            stb         Music_Sample            * 5
  ENDC
 ENDC
 IFGE MUSIC_VOICES-3
            * --- Voice 2 ---
            ldd         Music_PhaseInc2         * 6
            addd        Music_PhaseAccum2       * 7
            std         Music_PhaseAccum2       * 6
            tfr         a,b                     * 6
            andb        #$7F                    * 2
            ldx         Music_WavePtr2          * 6
            abx                                 * 3
            ldb         ,x                      * 4
            tsta                                * 2
            bpl         V2Store@                * 3
            negb                                * 2
V2Store@    subb        #$80                    * 2
            addb        Music_Sample            * 5
 ENDC
 IFGE MUSIC_VOICES-2
            orb         #2
            stb         ,u+
 ENDC
            cmpu        #Sound_PageBuffer+256
            bne         MusicRefillLoop@
            rts

* --- Fade-out path ---
MusicRefillFade
            andcc       #$AF
MusicFadeLoop@
            * --- Voice 0 ---
            ldd         Music_PhaseInc0
            addd        Music_PhaseAccum0
            std         Music_PhaseAccum0
            tfr         a,b
            andb        #$7F
            ldx         Music_WavePtr0
            abx
            ldb         ,x
            tsta
            bpl         FV0Store@
            negb
FV0Store@
 IFEQ MUSIC_VOICES-1
            stb         Music_Sample            * save for fade check
 ELSE
            stb         Music_Sample
 ENDC
 IFGE MUSIC_VOICES-2
            * --- Voice 1 ---
            ldd         Music_PhaseInc1
            addd        Music_PhaseAccum1
            std         Music_PhaseAccum1
            tfr         a,b
            andb        #$7F
            ldx         Music_WavePtr1
            abx
            ldb         ,x
            tsta
            bpl         FV1Store@
            negb
FV1Store@   subb        #$80
            addb        Music_Sample
  IFGE MUSIC_VOICES-3
            stb         Music_Sample
  ENDC
 ENDC
 IFGE MUSIC_VOICES-3
            * --- Voice 2 ---
            ldd         Music_PhaseInc2
            addd        Music_PhaseAccum2
            std         Music_PhaseAccum2
            tfr         a,b
            andb        #$7F
            ldx         Music_WavePtr2
            abx
            ldb         ,x
            tsta
            bpl         FV2Store@
            negb
FV2Store@   subb        #$80
            addb        Music_Sample
 ENDC
            * B = final mixed sample — check if near center
            cmpb        #$7C
            blo         MusicFadeStore@
            cmpb        #$84
            blo         MusicFadeCross@
MusicFadeStore@
            orb         #2
            stb         ,u+
            cmpu        #Sound_PageBuffer+256
            bne         MusicFadeLoop@
            rts
MusicFadeCross@
            lda         #$82
MusicFadeFill@
            sta         ,u+
            cmpu        #Sound_PageBuffer+256
            bne         MusicFadeFill@
            clr         Music_Playing
            rts


***********************************************************
* Music_MixIntoBuffer_Impl
*   Add music samples into an existing Sound_PageBuffer.
*   Same optimizations as RefillBuffer.
***********************************************************
*
Music_MixSample     rmb     1
*
Music_MixIntoBuffer_Impl
            ldu         #Sound_PageBuffer
            lda         Music_Playing
            cmpa        #2
            beq         MusicMixFade
            andcc       #$AF
MusicMixLoop@
            * --- Voice 0 (raw value, subtract $80 for signed offset) ---
            ldd         Music_PhaseInc0
            addd        Music_PhaseAccum0
            std         Music_PhaseAccum0
            tfr         a,b
            andb        #$7F
            ldx         Music_WavePtr0
            abx
            ldb         ,x
            tsta
            bpl         MV0Store@
            negb
MV0Store@   subb        #$80
 IFEQ MUSIC_VOICES-1
            addb        ,u                      * 1 voice: add directly to SFX
            stb         ,u+
 ELSE
            stb         Music_MixSample
 ENDC
 IFGE MUSIC_VOICES-2
            * --- Voice 1 ---
            ldd         Music_PhaseInc1
            addd        Music_PhaseAccum1
            std         Music_PhaseAccum1
            tfr         a,b
            andb        #$7F
            ldx         Music_WavePtr1
            abx
            ldb         ,x
            tsta
            bpl         MV1Store@
            negb
MV1Store@   subb        #$80
            addb        Music_MixSample
  IFGE MUSIC_VOICES-3
            stb         Music_MixSample
  ENDC
 ENDC
 IFGE MUSIC_VOICES-3
            * --- Voice 2 ---
            ldd         Music_PhaseInc2
            addd        Music_PhaseAccum2
            std         Music_PhaseAccum2
            tfr         a,b
            andb        #$7F
            ldx         Music_WavePtr2
            abx
            ldb         ,x
            tsta
            bpl         MV2Store@
            negb
MV2Store@   subb        #$80
            addb        Music_MixSample
 ENDC
 IFGE MUSIC_VOICES-2
            addb        ,u
            stb         ,u+
 ENDC
            cmpu        #Sound_PageBuffer+256
            bne         MusicMixLoop@
            rts

